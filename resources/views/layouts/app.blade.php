$(document).ready(function() {
    $('.date').datepicker({  
    format: 'yyyy-mm-dd',  
}).on('changeDate', function(e) { 
    $(this).datepicker('hide'); 
    
});  

$(document).on('change', '.date', function() {
    $(this).datepicker('hide'); 
    var search_date =$(this).val();
    if(search_date == ''){
        search_date = null;
    }
    $.ajax({
      url: '/search/' + search_date,
      type: 'get',
      data: {},
      success: function(data) {
        $("table tbody").html('');
        console.log('test',data);
        var  markup = '';
        if(data && data.length > 0){
            $.each(data, function (key, val) { 
                 markup += '<tr><td ><img src="/images/'+val['image'] + '" width="75"></td><td>'+val['title'] + '</td><td>'+val['categories'] + '</td><td>'+val['content'] + '</td><td>'+val['created_at'] + '</td></tr>';
                
            });
         
        }else{
            var  markup = '<tr><td colspan="5" class="text-center">No blogs found</td></tr>';  
        }
        
        $("table tbody").append(markup);
        
   }
   });
});

$('input[name="search_date"]').attr("placeholder","Enter Date");
